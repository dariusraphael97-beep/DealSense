// ── Lookup helper: try ZIP3, fallback to interpolation ──────────────
function getZipCoords(zip: string): [number, number] | null {
  const z3 = zip.slice(0, 3);
  if (ZIP3[z3]) return ZIP3[z3];

  // Fallback: try nearby prefixes
  const num = parseInt(z3, 10);
  for (let offset = 1; offset <= 5; offset++) {
    const lo = String(num - offset).padStart(3, "0");
    const hi = String(num + offset).padStart(3, "0");
    if (ZIP3[lo]) return ZIP3[lo];
    if (ZIP3[hi]) return ZIP3[hi];
  }
  return null;
}

// ── Component ───────────────────────────────────────────────────────
interface USMapPinProps {
  zipCode: string;
  className?: string;
}

export function USMapPin({ zipCode, className = "" }: USMapPinProps) {
  const coords = getZipCoords(zipCode);
  if (!coords) return null;

  const [lat, lng] = coords;

  // Skip non-continental US (AK, HI, PR)
  if (lat > 50 || lat < 24 || lng < -130 || lng > -65) return null;

  const [px, py] = project(lat, lng);

  // Clamp to viewBox
  const cx = Math.max(4, Math.min(196, px));
  const cy = Math.max(4, Math.min(126, py));

  return (
    <div className={`inline-flex items-center ${className}`} title={`ZIP ${zipCode}`}>
      <svg
        viewBox="0 0 200 130"
        className="w-12 h-8"
        role="img"
        aria-label={`ZIP ${zipCode}`}
      >
        {/* US outline */}
        <path
          d={US_OUTLINE}
          fill="var(--ds-map-fill, rgba(99,102,241,0.08))"
          stroke="var(--ds-map-stroke, rgba(99,102,241,0.22))"
          strokeWidth="1"
          strokeLinejoin="round"
        />

        {/* Pin glow */}
        <circle cx={cx} cy={cy} r="8" fill="rgba(99,102,241,0.20)" />

        {/* Pin dot */}
        <circle cx={cx} cy={cy} r="3" fill="#6366f1" />
        <circle cx={cx} cy={cy} r="1.2" fill="white" opacity="0.85" />
      </svg>
    </div>
  );
}
